public class UnaryDemo {
	
	public static void main(String[] args) {
		
		int Result = 1;
		System.out.println(Result);
		
		Result ++;
		System.out.println(Result);
		
		Result --;
		System.out.println(Result);
		
		Result = -Result ;
		System.out.println(Result);
		
		boolean success = false ;
		System.out.println(success);
		System.out.println(!success);
	}
	
}



