package seleniumBasics;

public class ConcatenationDemo {
	
	public static void main(String[] args) {
		
		String Firststring = "My Name is";
		String Secondstring = " Ronika Rout";
		String Thirdstring = "I am Angel";
		String Fourthstring = Firststring+Secondstring;
		String Fifthstring = "I love my food";
		System.out.println(Fourthstring + "\n" + Thirdstring + "\n" + Fifthstring);
				
	}

}
