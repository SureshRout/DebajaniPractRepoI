package seleniumBasics;

public class IfPractise {
	public static void main(String[] args) {
		int a = 8;
		if(a > 10) {
			System.out.println("Yes,It is True");
			
		}
		else if(a > 15) {
			System.out.println("No,It is False");
		}
		else {
			System.out.println("None of the above is True");
			
		}
	}
	

}
